void choose_position(int col, int row){
	if(col >= 0 && col < MAXCOL && row >= 0 && row < MAXROW){
		column_select(col);
		row_select(row);
	}
	delay(tdelay1,tdelay2);
}
void array_passage(int num1, int num2, char array[num1][num2], int size, int max) // passagin 2D array
{
	for(int i = 0; i<size; i++){
		for(int j = 0; j < MAXROW; j++){
			if(array[j][i] == '1'){
				choose_position(max-i, 7-j);
			}
		}
	}
	PTA->PDOR = 0x00; //clear register
}

